def type_multiplier(attacker_types: List["PokemonType"], defender_types: List["PokemonType"]) -> float:
    mul = 1
    for at in attacker_types:
        for dt in defender_types:
            mul *= type_chart[int(at.value)][int(dt.value)]
    return mul
